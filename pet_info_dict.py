# Сбор информации о питомце от пользователя и вывод её в формате строки

# Пустой словарь
pets = {}

# Ввод информации от пользователя
pet_name = input("Введите имя питомца: ")
pet_type = input("Введите вид питомца (например, 'собака', 'кошка'): ")
pet_age = int(input("Введите возраст питомца: "))
owner_name = input("Введите имя владельца: ")

# Запись информации в словарь
pets[pet_name] = {
    "Вид питомца": pet_type,
    "Возраст питомца": pet_age,
    "Имя владельца": owner_name
}

# Определение правильного склонения слова "год"
age = pets[pet_name]["Возраст питомца"]
if 11 <= age % 100 <= 14:
    years_str = "лет"
elif age % 10 == 1:
    years_str = "год"
elif 2 <= age % 10 <= 4:
    years_str = "года"
else:
    years_str = "лет"

# Формируем строку описания
description = f'Это {pets[pet_name]["Вид питомца"]} по кличке "{pet_name}". ' \
              f'Возраст питомца: {age} {years_str}. ' \
              f'Имя владельца: {pets[pet_name]["Имя владельца"]}'

# Выводим результат
print(description)