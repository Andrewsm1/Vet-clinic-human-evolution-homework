# Ввод N чисел по одному на строке и вывод их в обратном порядке

# Чтение количества чисел
n = int(input())

# Чтение чисел и сохранение в список
numbers = [int(input()) for _ in range(n)]

# Переворачиваем список
reversed_numbers = numbers[::-1]

# Выводим числа в одной строке через пробел
print(*reversed_numbers)