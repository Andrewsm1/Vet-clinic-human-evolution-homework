# Находим минимальное число лодок, чтобы перевезти всех рыбаков

# Максимальный вес лодки
m = int(input())

# Количество рыбаков
n = int(input())

# Вес каждого рыбака
weights = [int(input()) for _ in range(n)]

# Сортируем веса по возрастанию
weights.sort()

# Указатели на лёгкого и тяжёлого рыбаков
i = 0
j = n - 1

# Счётчик лодок
boats = 0

# Пока есть рыбаки
while i <= j:
    # Если лёгкий + тяжёлый помещаются в лодку
    if weights[i] + weights[j] <= m:
        i += 1  # лёгкий садится
    # В любом случае тяжёлый садится (в одиночку или с лёгким)
    j -= 1
    boats += 1  # лодка уходит

# Выводим минимальное количество лодок
print(boats)