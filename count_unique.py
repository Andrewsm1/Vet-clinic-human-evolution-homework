# Подсчёт количества различных чисел

# Ввод количества чисел
n = int(input())

# Ввод самих чисел через пробел и преобразование в список
numbers = list(map(int, input().split()))

# Используем множество (set), чтобы оставить только уникальные значения
unique_numbers = set(numbers)

# Выводим количество различных чисел
print(len(unique_numbers))